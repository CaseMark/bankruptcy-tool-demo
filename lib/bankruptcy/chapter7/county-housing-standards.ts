/**
 * Get county housing standard with fallback to state average
 *
 * @param state - Two-letter state code (e.g., "CA", "TX")
 * @param county - County name (case-insensitive)
 * @param householdSize - Number of people in household (1-5+)
 * @returns Monthly housing allowance amount
 */
export function getCountyHousingStandard(
  state: string,
  county: string | null | undefined,
  householdSize: number
): number {
  const normalizedState = state.toUpperCase();
  const normalizedCounty = county?.toUpperCase().replace(' COUNTY', '').trim();

  // Clamp household size to valid range (1-5)
  const sizeIndex = Math.min(Math.max(householdSize, 1), 5) - 1;

  // Try to get county-specific data
  const stateData = COUNTY_HOUSING_STANDARDS[normalizedState];
  if (stateData && normalizedCounty) {
    const countyData = stateData[normalizedCounty];
    if (countyData) {
      return countyData[sizeIndex];
    }
  }

  // Fallback: Calculate state average if we have state data but no county match
  if (stateData) {
    const counties = Object.values(stateData);
    if (counties.length > 0) {
      const sum = counties.reduce((acc, c) => acc + c[sizeIndex], 0);
      return Math.round(sum / counties.length);
    }
  }

  // Final fallback: National default
  const NATIONAL_DEFAULTS = [1800, 2100, 2200, 2450, 2500];
  return NATIONAL_DEFAULTS[sizeIndex];
}

/**
 * Check if county-level data is available for a state
 */
export function hasCountyData(state: string): boolean {
  return state.toUpperCase() in COUNTY_HOUSING_STANDARDS;
}

/**
 * Get list of counties available for a state
 */
export function getCountiesForState(state: string): string[] {
  const stateData = COUNTY_HOUSING_STANDARDS[state.toUpperCase()];
  return stateData ? Object.keys(stateData) : [];
}
